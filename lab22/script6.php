<?php
// Определение пользовательской функции
function custom_function($x, $y) {
    // Вычисление выражения по заданной формуле
    $c = atan($x - (3/5)*exp($x*$y) + ((pow($x+$y, $x))/pow(sin($x)+1.5, 2)));
    return $c; // Возвращаем результат
}

// Пример использования функции
$x = 2;
$y = 3;
$result = custom_function($x, $y);
echo "Результат вычислений для x=$x и y=$y: $result";
?>
